package j03_연산자;

public class Operation3 {

	public static void main(String[] args) {

		// 증감연산자
		
		int x = 10;
		System.out.println(x);
		
		x++;
		System.out.println(x);
		
		++x;
		System.out.println(x);
		System.out.println();
		
		
		int y = 20;
		System.out.println(y);
		System.out.println(y++);
		System.out.println(y);
		System.out.println(++y);
		System.out.println(y);
		System.out.println();
		
		System.out.println(y);
		System.out.println(y--);
		System.out.println(y);
		System.out.println(--y);
		System.out.println(y);
		
		
	}

}
