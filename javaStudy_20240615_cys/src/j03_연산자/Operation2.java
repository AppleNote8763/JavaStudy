package j03_연산자;

public class Operation2 {

	public static void main(String[] args) {

		// 복합대입연산자
		
		int result = 10;
		System.out.println(result);
		
		// result = result + 10;
		result += 10;
		System.out.println(result);
		
		// result = result / 2;
		result /= 2;
		System.out.println(result);

		// result = result % 3;
		result %= 3;
		System.out.println(result);

		
	}

}
